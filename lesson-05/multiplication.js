let newBooleanVariable1 = true;

let newStringVariable1 = 'Test';

let newNumberVariable1 = 10;

console.log(newStringVariable1 * newBooleanVariable1);  //string * boolean

console.log(newStringVariable1 * newNumberVariable1);  //string * number

console.log(newNumberVariable1 * newBooleanVariable1);  //number * boolean